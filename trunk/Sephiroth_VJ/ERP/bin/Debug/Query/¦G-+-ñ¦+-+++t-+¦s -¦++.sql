SELECT FN_SBC_ITEM_GROUP_NAME(SUBSTR(GROUP_CD,1,2), SUBSTR(GROUP_CD,3,1), '00') AS FIRST_CLASS,
       FN_SBC_ITEM_GROUP_NAME(SUBSTR(GROUP_CD,1,2), SUBSTR(GROUP_CD,3,1), SUBSTR(GROUP_CD,4,2) ) AS SECOND_CLASS,
       ITEM_NAME1, USE_YN, IMPORT_DS, IMPORT_QD, IMPORT_VJ,
       CUS_CD_DS,
       PKG_SBC_COMMON.FN_GET_CUSTOMER('DS', CUS_CD_DS) AS CUST_NAME, 
       UPD_USER, UPD_YMD
  FROM SBC_ITEM
 WHERE GROUP_CD LIKE  [1;그룹코드;T] || '%' 
 ORDER BY GROUP_CD